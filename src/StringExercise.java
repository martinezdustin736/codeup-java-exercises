import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(education);

        String checkString = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkString);

        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);

        String back = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(back);

    }
}
